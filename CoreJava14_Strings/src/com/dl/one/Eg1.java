package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		
		
		//Strings : Strings are Sequence of characters
		
		char ch[] = {'S','r','i','n','i','v','a','s','u'};
		System.out.println(ch);
		
		//Strings are immutable, once you created it cannot modify
		
		//Ways to create a String
		//2 ways
		
		//using literals
		String s1 = "Srinivasu";
		System.out.println(s1);
		
		//Using new Keyword
		String s2 = new String("Srinivasu");
		System.out.println(s2);

	}

}
